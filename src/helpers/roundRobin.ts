type IndexedRound = typeof fourPlayerIndexedRounds | typeof fivePlayerIndexedRounds;

type Match = { teamA: [string, string]; teamB: [string, string] };
type Round = { matches: Match[]; byes: string[] };

function generate(players: string[]): Round[] {
  shuffle(players);

  const indexedRoundsByCount: Record<number, IndexedRound> = {
    4: fourPlayerIndexedRounds,
    5: fivePlayerIndexedRounds,
    6: sixPlayerIndexedRounds,
    7: sevenPlayerIndexedRounds,
    8: eightPlayerIndexedRounds,
    9: ninePlayerIndexedRounds,
    10: tenPlayerIndexedRounds,
    11: elevenPlayerIndexedRounds,
    12: twelvePlayerIndexedRounds,
    13: thirteenPlayerIndexedRounds,
    14: fourteenPlayerIndexedRounds,
    15: fifteenPlayerIndexedRounds,
    16: sixteenPlayerRounds,
  };

  const indexedRounds = indexedRoundsByCount[players.length];
  if (!indexedRounds) {
    throw new Error(`Unsupported number of players: ${players.length}`);
  }

  return indexedRounds.map((round) => {
    const matches = round.games.map(([[i1a, i1b], [i2a, i2b]]) => ({
      teamA: [players[i1a], players[i1b]] as [string, string],
      teamB: [players[i2a], players[i2b]] as [string, string],
    }));

    const byes = round.bye ? round.bye.map(i => players[i]) : [];

    return { matches, byes };
  });
}

export { generate };
export type { Round, Match };
